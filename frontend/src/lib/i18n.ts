export function translate(key: string, language: AppLanguage) {
  if (language === "vi") return enToVi[key] ?? key;
  return viToEn[key] ?? key;
}

export function translateRole(role: string, language: AppLanguage) {
  if (language === "vi") return roleLabel[role] ?? role;
  return englishRoleLabel[role] ?? role;
}
